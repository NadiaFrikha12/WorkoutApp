    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            Connection con = ConnectionProvider.orclConnection();

            String query = "SELECT muscle_cible, exercise, serie, repetition, poids FROM exercise WHERE username_ex = ? AND TRUNC(date_ex) = TRUNC(?) ";
            PreparedStatement ps = con.prepareStatement(query);

            java.util.Date utilDate = (java.util.Date) date_sp.getValue();
            java.sql.Date sqlDate = new java.sql.Date(utilDate.getTime());

            ps.setString(1, currentUsername);
            ps.setDate(2, sqlDate); // sqlDate est tronqu√©e automatiquement (sans heure)

            ResultSet rs = ps.executeQuery();

            
            DefaultTableModel model = (DefaultTableModel) Table.getModel();
            model.setRowCount(0);

            int count = 0;
            while (rs.next()) {
                count++;
                model.addRow(new Object[]{
                    rs.getString("muscle_cible"),
                    rs.getString("exercise"),
                    rs.getInt("serie"),
                    rs.getInt("repetition"),
                    rs.getDouble("poids")
                });
            }

            if (count == 0) {
                JOptionPane.showMessageDialog(this, "No workout found for this date.");
            }

            rs.close();
            ps.close();
            con.close();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erreur : " + e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

